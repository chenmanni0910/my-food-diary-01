export function matchCalories(description: string): { match: string; calories: number } | null {
  const input = description.toLowerCase().trim();
  
  // Sort by key length descending to match longer phrases first
  const sortedKeys = Object.keys(foodDatabase).sort((a, b) => b.length - a.length);
  
  for (const key of sortedKeys) {
    if (input.includes(key)) {
      return { match: key, calories: foodDatabase[key] };
    }
  }
  
  return null;
}
